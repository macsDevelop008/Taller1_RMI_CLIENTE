    private void btnUpdatePlayerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdatePlayerActionPerformed
       
        JugadorVO jvo = null;
        String id;
        String cuen;
        String con;
        String apo;
        String email;
        double reg = 1;
        Date da = new Date();
        
        id = txtIdPlayer.getText().trim();
        cuen = txtAccPlayer.getText().trim();
        con = txtPassPlayer.getText().trim();
        apo = txtNickPlayer.getText().trim();
        email = txtEmailPlayer.getText().trim();
        
        jvo = new JugadorVO(id, cuen, con, apo, email, reg, da);
        try
        {
            servicioInterface.updatePlayer(jvo);
            JOptionPane.showMessageDialog(this, "Player update");
        } catch (RemoteException ex) {
            Logger.getLogger(GUIUpdatePlayer.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        
        
    }//GEN-LAST:event_btnUpdatePlayerActionPerformed

